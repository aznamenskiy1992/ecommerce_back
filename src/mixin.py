class MixinLog:
    """Миксин для логирования создания объектов товаров.

    Добавляет функциональность логирования при инициализации объектов.
    При создании экземпляра класса выводит в консоль информацию о товаре.

    Методы:
        __init__: Инициализация с логированием
        __repr__: Формирование строки представления объекта

    Применение:
        class Product(MixinLog, OtherBaseClasses):
            ...
    """

    def __init__(self):
        """Инициализирует миксин и логирует создание объекта.

        Примечания:
            1. Вызывает __init__ родительских классов через super()
            2. Автоматически выводит repr(self) в консоль при создании
        """
        super().__init__()  # Вызов __init__ у следующих классов в MRO
        print(repr(self))  # Вывод информации о созданном объекте

    def __repr__(self):
        """Формирует строковое представление объекта для логирования.

        Returns:
            str: Строка в формате:
                 "ClassName (name, description, price, quantity)"

        Пример:
            "Smartphone (iPhone 13, Смартфон, 79990, 5)"
        """
        return f"{self.__class__.__name__} " f"({self.name}, {self.description}, " f"{self.price}, {self.quantity})"
